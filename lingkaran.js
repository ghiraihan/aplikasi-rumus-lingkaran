function TentukanLuasDanKelilingSegitigaSamaSisi (a,t) {
  const alas = a;
  const tinggi = t;
 
 //Proses//
  const Luas = (alas*tinggi)/2
  const Keliling = 3*alas
 
 //Hasil//
  console.log("Alas:", alas,"cm");
  console.log("Tinggi:", tinggi,"cm");
  console.log("Luas:", Luas,"cm^2");
  console.log("Keliling Segitiga:", Keliling,"cm");
 }
 
 TentukanLuasDanKelilingSegitigaSamaSisi (5, 10);
 